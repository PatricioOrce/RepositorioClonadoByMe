
function mostrar()
{

let perimetro;
let alturaRectangulo;
let anchoRectangulo;

alturaRectangulo=parseInt(prompt("Ingrese la altura del rectangulo: "+alturaRectangulo));
anchoRectangulo=parseInt(prompt("Ingrese el ancho del rectangulo: "+anchoRectangulo));

perimetro=(alturaRectangulo+anchoRectangulo)*2;

alert("El perimetro del rectangulo es: "+perimetro);


}
